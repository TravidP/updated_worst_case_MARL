#!/usr/bin/env python3
"""Create per-controller group plots from existing raw evaluation CSV files.

This script does not run any SUMO evaluations. It rebuilds the
`*_queue_by_group.png` and `*_speed_by_group.png` figures directly from
`*_queue_raw.csv` and `*_speed_raw.csv` files generated by
`eval_signal_controllers.py`.

Example:
    python3 plot_groups_per_controller.py
    python3 plot_groups_per_controller.py --controllers iqll_marl,iqll_retrained
"""

